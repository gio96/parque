    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
            // TODO add your handling code here:
            
            Conexion cn= new Conexion();
            cn.getConexion();
            String nom= nombre.getText();
            String cont=new String(contrasena.getPassword());
            //System.out.println(cont);
            ResultSet rs= cn.consultaGeneral("select id,nombre,permiso from usuarios where nombre='"+nom+"' and contrasena='"+cont+"'");
            
            //usuario us= new usuario();
            
        try {  
            
               while (rs.next()) {
                   us.setId(rs.getInt(1));
                   us.setNombre(rs.getString(2));
                   us.setPermiso(rs.getInt(3));
                } 
            
        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            //JOptionPane.showMessageDialog(null, "El usuario NO existe");
        }
        
        if(us.getPermiso() != null)
        {
            if(us.getPermiso()==1)
               {
                permisos=true; 
                Visual v= new Visual();
                v.setVisible(true);
                dispose(); 
               }else if(us.getPermiso()==0)
               {
                    permisos=false; 
                    Visual v= new Visual();
                    v.setVisible(true);
                    dispose();
               }
        }else{
            JOptionPane.showMessageDialog(null, "Datos invalidos login","Error",JOptionPane.ERROR_MESSAGE);
            nombre.setText("");
            contrasena.setText("");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

